package com.example.myapplication.ui.TeacherProof;

public class CardDisplay {
    private String teacherName;
    private String courseName;
    private String listened_teacherName;
    private String Date;


    public String getTeacherName() {
        return teacherName;
    }

    public void setTeacherName(String teacherName) {
        this.teacherName = teacherName;
    }

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public String getListened_teacherName() {
        return listened_teacherName;
    }

    public void setListened_teacherName(String listened_teacherName) {
        this.listened_teacherName = listened_teacherName;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }
}
